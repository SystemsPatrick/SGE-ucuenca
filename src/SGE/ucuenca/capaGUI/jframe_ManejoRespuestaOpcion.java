    private void jButton_eliminarRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_eliminarRegistroActionPerformed

        Object objRespOpMultiple = new Respuesta_Opcion(
            ClaseValidacion.getNumero(jTextField_cod_encuesta1.getText()),
            ClaseValidacion.getNumero(jTextField_cod_pregunta1.getText()),
            ClaseValidacion.getNumero(jTextField_cod_opmultiple1.getText()),
            ClaseValidacion.getNumero(jTextField_cod_usuario1.getText()),
            jTextField_respuestaOpcion.getText());

        if (datosRecuperados.eliminarRegistros(objRespOpMultiple)) {
            //Mostrar Ventana Emergente
            int result = JOptionPane.showConfirmDialog(null, "MENSAJE: \n\nRegistro Eliminado", "Aviso!", JOptionPane.OK_CANCEL_OPTION);
        }
    }//GEN-LAST:event_jButton_eliminarRegistroActionPerformed

