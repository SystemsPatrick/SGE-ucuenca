    private void jButton_crearUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_crearUsuarioActionPerformed
        
        char[] var = jPasswordField_contraseñaNuevoUsuario.getPassword();
        String getPaswordString = new String(var);
        
        if (jRadioButton_admin.isSelected()) {
            if (conectarSystem.crearUsuariosSQL(jTextField_nombreNuevoUsuario.getText(), getPaswordString, 0)) {
                int result = JOptionPane.showConfirmDialog(null, "MENSAJE: \n\tNuevo Administrador Creado", "Aviso!", JOptionPane.OK_CANCEL_OPTION);
            }
        }
        if (jRadioButton_enc.isSelected()) {
            if (conectarSystem.crearUsuariosSQL(jTextField_nombreNuevoUsuario.getText(), getPaswordString, 1)) {
                int result = JOptionPane.showConfirmDialog(null, "MENSAJE: \n\tNuevo Encuestador Creado", "Aviso!", JOptionPane.OK_CANCEL_OPTION);
            }
        }
        
        if (jRadioButton_cliente.isSelected()) {
            if (conectarSystem.crearUsuariosSQL(jTextField_nombreNuevoUsuario.getText(), getPaswordString, 2)) {
                int result = JOptionPane.showConfirmDialog(null, "MENSAJE: \n\tNuevo Cliente Creado", "Aviso!", JOptionPane.OK_CANCEL_OPTION);
            }
        }
        
        
        
        jPasswordField_contraseñaNuevoUsuario.setText("");
        jTextField_nombreNuevoUsuario.setText("");
        
    }//GEN-LAST:event_jButton_crearUsuarioActionPerformed

