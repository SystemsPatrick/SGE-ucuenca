    private void jButton_asignarPrivilegiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_asignarPrivilegiosActionPerformed

        String opcion = (String)jComboBox_asignarPrivilegio.getSelectedItem();
        String usuarioSeleccionado = jList_listaAsignar.getSelectedValue();
        String cadena = "";
        if(opcion.equalsIgnoreCase("Crear vistas")){
            //Seleccionar, Crear usuarios, Eliminar usuarios, Modificar usuarios, Crear vistas
            cadena = "CREATE VIEW";
        }
        if(opcion.equalsIgnoreCase("Eliminar usuarios")){
            cadena = "DROP USER";
        }
        if(opcion.equalsIgnoreCase("Crear usuarios")){
            cadena = "CREATE USER";
        }
        if(opcion.equalsIgnoreCase("Modificar usuarios")){
            cadena = "ALTER USER";
        }
        
        conectarSystem.agregarPrivilegios(cadena, usuarioSeleccionado);

    }//GEN-LAST:event_jButton_asignarPrivilegiosActionPerformed

